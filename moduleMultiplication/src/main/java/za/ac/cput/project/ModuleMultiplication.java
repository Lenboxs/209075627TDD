public class ModuleMultiplication {
    public ModuleMultiplication() {
    }

    public double mul(double num1, double num2)
    {
        return num1 * num2;
    }

    public  int mul(int num1, int num2)
    {
        return num1 * num2;
    }

    public float mul(float num1, float num2)
    {
        return num1 * num2;
    }
}
