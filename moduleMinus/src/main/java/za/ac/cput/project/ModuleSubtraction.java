public class ModuleSubtraction {

    public ModuleSubtraction() {
    }

    public double sub(double num1, double num2)
    {
        return num1 - num2;
    }

    public  int sub(int num1, int num2)
    {
        return num1 - num2;
    }

    public float sub(float num1, float num2)
    {
        return num1 - num2;
    }
}
