public class ModuleDivision {

    public ModuleDivision() {

    }

    public double div(double num1, double num2)
    {
        return num1 / num2;
    }

    public  int div(int num1, int num2)
    {
        return num1 / num2;
    }

    public float div(float num1, float num2)
    {
        return num1 / num2;
    }

}
